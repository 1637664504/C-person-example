#include <stdio.h>
typedef unsigned char uint8_t;

uint8_t XorCheckSum(uint8_t *buf, uint8_t len)
{
    uint8_t i=0;
    uint8_t *p = 0;
    char checksum = 0;
    p = buf;
    checksum = *p;
    p++;
    for(i=1; i<len; i++)
    {
        checksum ^= *p;
        p++;
    }

    return checksum;
}

int main(void)
{
    uint8_t a[]={0x68,0x0c, 0xf3, 0x00, 0x00, 0x00, 0x00, 0x00,0x00,0x00};
    uint8_t b[]={0x68,0x0c, 0xf3, 0x00, 0x01, 0x00, 0x00, 0x00,0x00,0x00};
    uint8_t c[]={0x68,0x0c, 0xf3, 0x00, 0x10, 0x00, 0x00, 0x00,0x00,0x00};
    uint8_t d[]={0x68,0x0c, 0xf3, 0x00, 0x11, 0x00, 0x00, 0x00,0x00,0x00};

    printf("0x%02x,0x%02x,0x%02x,0x%02x\n",XorCheckSum(a,10),
        XorCheckSum(b,10),
        XorCheckSum(c,10),
        XorCheckSum(d,10));

    return 0;
}